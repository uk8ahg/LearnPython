s=input("Введите строку\n")
print('Третий символ:',s[2:3])
print('Предпоследний символ:',s[-2])
print('5 символов:',s[0:5])
print('Без двух последних:',s[0:len(s)-2])
print('С четными индексами:',s[::2])
print('С нечетными индексами:',s[1::2])
print('Обратный порядок:',s[::-1])
print('Через один в обратном порядке:',s[::-2])
print('Длина строки:',len(s))
